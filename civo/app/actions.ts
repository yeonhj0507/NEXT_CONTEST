export const getNewListAction = async () => {
  const supabase = await createClient();

  const { data, error } = await supabase
    .from("posts")
    .select("*")
    .order("created_at", { ascending: false });

  if (error) {
    console.error("뉴스 리스트 에러:", error.message);
    return [];
  }

  return data;
};

